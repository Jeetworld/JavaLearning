package JavaLearning;

public class OperatorsLearning {
	public static void main(java.lang.String[] args) {
		
		//12+13 --in this statement 12 and 13 are Operands
		//Unary Operator  --1 operands -- ++ ++ !
		//Binary Operator --2 operands
		//Ternary Operator --3 operands
		
		// && --And --both conditions should statify otherwise gives false 
		// || --Or == either of consitions should be true
		
			System.out.println(true || true ); // true 
			System.out.println((2==2) || true ); //either of condition should be true
			System.out.println((3==4) && true ); // both should be true to get Tru output
	}
}
